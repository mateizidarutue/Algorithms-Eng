#pragma once

#include <cstdint>

// Hash function for reproducibility ensuring uniform distribution of hash outputs
inline uint64_t custom_hash(uint64_t key) {
    key ^= key >> 33;
    key *= 0xff51afd7ed558ccdULL;
    key ^= key >> 33;
    key *= 0xc4ceb9fe1a85ec53ULL;
    key ^= key >> 33;
    return key;
}